# ── Request Handler ──────────────────────────────────────────────
class WizardHandler(http.server.BaseHTTPRequestHandler):

    def log_message(self, fmt, *args):
        # Suppress noisy access logs; only print errors
        if args and str(args[1]) not in ("200", "204"):
            super().log_message(fmt, *args)

    def _send_json(self, data: dict, status: int = 200):
        body = json.dumps(data).encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(body)))
        self.send_header("Access-Control-Allow-Origin", "*")
        self.end_headers()
        self.wfile.write(body)

    def _send_html(self, content: bytes):
        self.send_response(200)
        self.send_header("Content-Type", "text/html; charset=utf-8")
        self.send_header("Content-Length", str(len(content)))
        self.end_headers()
        self.wfile.write(content)

    def _read_body(self) -> dict:
        length = int(self.headers.get("Content-Length", 0))
        raw = self.rfile.read(length)
        return json.loads(raw.decode("utf-8")) if raw else {}

    def do_OPTIONS(self):
        self.send_response(204)
        self.send_header("Access-Control-Allow-Origin", "*")
        self.send_header("Access-Control-Allow-Methods", "GET, POST, OPTIONS")
        self.send_header("Access-Control-Allow-Headers", "Content-Type")
        self.end_headers()

    def do_GET(self):
        parsed = urllib.parse.urlparse(self.path)
        path = parsed.path

        if path in ("/", "/index.html"):
            if WIZARD_HTML.exists():
                self._send_html(WIZARD_HTML.read_bytes())
            else:
                self._send_json({"error": "index.html not found"}, 404)

        elif path == "/api/status":
            self._send_json(get_status())

        else:
            self._send_json({"error": "Not found"}, 404)

    def do_POST(self):
        parsed = urllib.parse.urlparse(self.path)
        path = parsed.path

        if path == "/api/save-config":
            try:
                config = self._read_body()

                set_status("configuring", "Writing configuration files...")

                # Validate required field
                if not config.get("anthropicKey", "").strip():
                    self._send_json({"ok": False, "error": "Anthropic API key is required"}, 400)
                    return

                # Ensure workspace exists
                WORKSPACE.mkdir(parents=True, exist_ok=True)

                env_path = write_env_file(config)
                oc_path = write_openclaw_config(config)

                set_status("configured", "Configuration saved")

                self._send_json({
                    "ok": True,
                    "envPath": str(env_path),
                    "openclawConfigPath": str(oc_path),
                })

            except json.JSONDecodeError:
                self._send_json({"ok": False, "error": "Invalid JSON"}, 400)
            except Exception as exc:
                self._send_json({"ok": False, "error": str(exc)}, 500)

        elif path == "/api/complete":
            try:
                WORKSPACE.mkdir(parents=True, exist_ok=True)
                COMPLETE_FLAG.touch()
                set_status("complete", "Setup complete")
                self._send_json({"ok": True})

                # Shut down server after response is flushed
                threading.Thread(target=self._shutdown_server, daemon=True).start()

            except Exception as exc:
                self._send_json({"ok": False, "error": str(exc)}, 500)

        else:
            self._send_json({"error": "Not found"}, 404)

    def _shutdown_server(self):
        import time
        time.sleep(0.5)
        self.server.shutdown()


# ── Main ─────────────────────────────────────────────────────────
def main():
    server = http.server.HTTPServer(("localhost", PORT), WizardHandler)
    print(f"Jarvis Setup Wizard running at http://localhost:{PORT}")
    print("Waiting for configuration...")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        pass
    finally:
        server.server_close()
    print("Setup wizard server stopped.")


if __name__ == "__main__":
    main()

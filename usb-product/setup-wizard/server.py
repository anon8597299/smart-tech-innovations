#!/usr/bin/env python3
"""
Jarvis Setup Wizard — HTTP Server
Stdlib only. No external dependencies.
Serves the wizard UI and handles config write + completion.
"""

import http.server
import json
import os
import pathlib
import platform
import re
import secrets
import sys
import threading
import urllib.parse
from http import HTTPStatus

# ── Paths ────────────────────────────────────────────────────────
SCRIPT_DIR = pathlib.Path(__file__).parent.resolve()
PROJECT_ROOT = SCRIPT_DIR.parent  # usb-product/
HOME = pathlib.Path.home()

WORKSPACE = HOME / "jarvis-workspace"
if platform.system() == "Windows":
    WORKSPACE = pathlib.Path("C:/Jarvis")

OPENCLAW_DIR = HOME / ".openclaw"
TEMPLATE_PATH = PROJECT_ROOT / "openclaw-template.json"
WIZARD_HTML = SCRIPT_DIR / "index.html"
COMPLETE_FLAG = WORKSPACE / ".setup-complete"

PORT = 9999

# Current status (read by GET /api/status)
_status = {"step": "collecting_keys", "message": "Waiting for configuration"}
_status_lock = threading.Lock()


def set_status(step: str, message: str):
    with _status_lock:
        _status["step"] = step
        _status["message"] = message


def get_status() -> dict:
    with _status_lock:
        return dict(_status)


# ── Config Writers ───────────────────────────────────────────────
def write_env_file(config: dict) -> pathlib.Path:
    """Write ~/jarvis-workspace/builder/.env"""
    env_dir = WORKSPACE / "builder"
    env_dir.mkdir(parents=True, exist_ok=True)
    env_path = env_dir / ".env"

    lines = [
        "# Jarvis — environment config",
        "# Generated by Jarvis Setup Wizard",
        "",
        f"ANTHROPIC_API_KEY={config.get('anthropicKey', '')}",
        f"GOOGLE_PLACES_API_KEY={config.get('googleKey', '')}",
        f"PERPLEXITY_API_KEY={config.get('perplexityKey', '')}",
        "",
        f"BUSINESS_NAME={config.get('businessName', '')}",
        f"BUSINESS_INDUSTRY={config.get('industry', '')}",
        f"BUSINESS_PHONE={config.get('phone', '')}",
        f"BUSINESS_EMAIL={config.get('email', '')}",
        f"BUSINESS_WEBSITE={config.get('website', '')}",
        "",
        "JARVIS_PORT=8080",
        "OPENCLAW_GATEWAY_PORT=18789",
    ]

    env_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return env_path


